// Function to update language file with new translations
function updateLanguageFile(langCode, translations) {
  const filePath = path.join(messagesDir, `${langCode}.json`);
  
  if (!fs.existsSync(filePath)) {
    console.log(`File ${langCode}.json does not exist, skipping...`);
    return;
  }
  
  const content = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  
  // Update common section with new keys
  if (!content.common) content.common = {};
  
  Object.keys(translations).forEach(key => {
    if (typeof translations[key] === 'object' && translations[key] !== null && !Array.isArray(translations[key])) {
      // Handle nested objects like subscription
      if (!content.common[key]) content.common[key] = {};
      Object.keys(translations[key]).forEach(nestedKey => {
        content.common[key][nestedKey] = translations[key][nestedKey];
      });
    } else {
      content.common[key] = translations[key];
    }
  });
  
  // Write back to file
  fs.writeFileSync(filePath, JSON.stringify(content, null, 2) + '\n', 'utf8');
  console.log(`✅ Updated ${langCode}.json`);
}

// Update all language files
Object.keys(newKeysTranslations).forEach(langCode => {
  updateLanguageFile(langCode, newKeysTranslations[langCode]);
});

